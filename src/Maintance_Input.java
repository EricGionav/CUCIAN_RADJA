    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
    // Mendapatkan nilai dari TextField dan ComboBox
    String idStokStr = txt_idp.getText();
    String namaStokStr = txt_namaPalat.getSelectedItem().toString();
    String jmlStokStr = txt_hargaPalat.getText();  // Mengambil jumlah stok
    String hargaStokStr = txt_tanggalPalat.getText(); // Seharusnya tanggal pemeliharaan alat

    // Validasi jika field kosong
    if (idStokStr.isEmpty() || namaStokStr.isEmpty() || jmlStokStr.isEmpty() || hargaStokStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validasi input numerik untuk jumlah stok dan harga stok
    try {
        int jumlahStok = Integer.parseInt(jmlStokStr);  // Mengubah String ke Integer
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        java.util.Date parsedDate = sdf.parse(hargaStokStr);  // Parsing tanggal pemeliharaan alat
        java.sql.Date tglStok = new java.sql.Date(parsedDate.getTime()); // Konversi ke java.sql.Date

        // Cek apakah pemeliharaan alat sudah ada
        String checkQuery = "SELECT tanggal_pemeliharaan, harga_pemeliharaanAlat FROM maintance WHERE pemeliharaan_alat = ?";

        try (Connection conn = Owner_StokInput.DatabaseConnection.getConnection();
             PreparedStatement checkPs = conn.prepareStatement(checkQuery)) {

            checkPs.setString(1, namaStokStr);
            try (ResultSet rs = checkPs.executeQuery()) {
                if (rs.next()) {
                    // Jika pemeliharaan alat sudah ada, update tanggal dan harga pemeliharaan
                    java.sql.Date existingTglStok = rs.getDate("tanggal_pemeliharaan");
                    double existingHargaStok = rs.getDouble("harga_pemeliharaanAlat");

                    // Update pemeliharaan alat
                    String updateQuery = "UPDATE maintance SET tanggal_pemeliharaan = ?, harga_pemeliharaanAlat = ? WHERE pemeliharaan_alat = ?";
                    try (PreparedStatement updatePs = conn.prepareStatement(updateQuery)) {
                        updatePs.setDate(1, tglStok); // Update tanggal pemeliharaan
                        updatePs.setDouble(2, existingHargaStok + jumlahStok); // Update harga pemeliharaan
                        updatePs.setString(3, namaStokStr);

                        int rowsUpdated = updatePs.executeUpdate();
                        if (rowsUpdated > 0) {
                            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                        }
                    }

                } else {
                    // Jika pemeliharaan alat belum ada, insert data baru
                    String insertQuery = "INSERT INTO maintance (id_pengeluaran, pemeliharaan_alat, tanggal_pemeliharaan, harga_pemeliharaanAlat) VALUES (?, ?, ?, ?)";
                    try (PreparedStatement insertPs = conn.prepareStatement(insertQuery)) {
                        insertPs.setString(1, idStokStr);
                        insertPs.setString(2, namaStokStr);
                        insertPs.setDate(3, tglStok);  // Menyimpan tanggal pemeliharaan
                        insertPs.setDouble(4, jumlahStok);

                        int rowsInserted = insertPs.executeUpdate();
                        if (rowsInserted > 0) {
                            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }
            }
            
            // Mendapatkan tanggal saat ini untuk reset
            java.util.Date currentDate = new java.util.Date();  // Mendapatkan tanggal saat ini
            sdf = new java.text.SimpleDateFormat("yyyy-MM-dd"); // Format yang digunakan
            txt_tanggalPalat.setText(sdf.format(currentDate)); // Reset Tanggal Stok ke tanggal saat ini

            // Membersihkan field setelah sukses
            txt_idp.setText("");
            txt_namaPalat.setSelectedIndex(0); // Reset ComboBox
            txt_hargaPalat.setText("");  // Reset Jumlah Stok
            txt_tanggalPalat.setText(""); // Reset Tanggal Pemeliharaan

        }

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Jumlah stok dan harga stok harus berupa angka!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (java.text.ParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Format tanggal tidak valid! Gunakan format yyyy-mm-dd.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage(), "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btn_simpanActionPerformed

