private void loadTable() {
    // Model untuk JTable
    DefaultTableModel model = new DefaultTableModel(new String[]{"ID Pengeluaran", "Harga Pemeliharaan Alat", "Nama Pemeliharaan Alat", "Tanggal Pemeliharaan"}, 0);
    String query = "SELECT * FROM maintance";
    String maxIdQuery = "SELECT MAX(id_pengeluaran) AS max_id FROM maintance";  // Query to get the max ID

    try (Connection conn = Owner_Stok.DatabaseConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(query);
         ResultSet rs = ps.executeQuery();
         PreparedStatement psMaxId = conn.prepareStatement(maxIdQuery);
         ResultSet rsMaxId = psMaxId.executeQuery()) {

        // Loop through the main data
        while (rs.next()) {
            int ids = rs.getInt("id_pengeluaran");
            int namas = rs.getInt("harga_pemeliharaanAlat");
            String jumlahs = rs.getString("pemeliharaan_alat");
            String hargas = rs.getString("tanggal_pemeliharaan");

            model.addRow(new Object[]{ids, namas, jumlahs, hargas});
        }

        // Handling the ID auto-increment logic
        if (rsMaxId.next()) {
            int nextId = rsMaxId.getInt("max_id") + 1;  // ID berikutnya
            txt_idp.setText(String.valueOf(nextId)); // Atur TextField dengan ID berikutnya
        } else {
            txt_idp.setText("1"); // Jika tidak ada data, mulai dari 1
        }

        // Set the table model to the JTable
        tabel_maintance.setModel(model); // Tampilkan data di JTable

        // Clear the text field
        txt_hargaPalat.setText("");

        // Reload current date and set it in a text field (example)
        java.util.Date currentDate = new java.util.Date();  // Mendapatkan tanggal saat ini
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");  // Format tanggal
        String formattedDate = sdf.format(currentDate);
        txt_tanggalPalat.setText(formattedDate);  // Assuming txt_tanggal is a JTextField where you want to display the date

    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
}


    
