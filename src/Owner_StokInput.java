private void setAutoIncrementId() {
    String query = "SELECT MAX(id_stok) AS last_id FROM stok";

    try (Connection conn = Owner_StokInput.DatabaseConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(query);
         ResultSet rs = ps.executeQuery()) {
        
        if (rs.next()) {
            int lastId = rs.getInt("last_id");
            int nextId = lastId + 1; // Tambahkan 1 ke ID terakhir
            txt_ids.setText(String.valueOf(nextId));
        } else {
            txt_ids.setText("1"); // Jika tidak ada data, mulai dari 1
        }
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengambil ID terakhir: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
}


private void setCurrentDate() {
    // Mendapatkan tanggal saat ini
    Date currentDate = new Date(System.currentTimeMillis());
    
    // Menampilkan tanggal ke TextField dalam format yyyy-MM-dd
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    tglStok.setText(sdf.format(currentDate)); // Format sesuai kebutuhan
}



 


