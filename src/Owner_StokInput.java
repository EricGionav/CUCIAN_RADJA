    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
    // Mendapatkan nilai dari TextField dan ComboBox
    String idStokStr = txt_ids.getText();
    String namaStokStr = txt_namaStok.getSelectedItem().toString();
    String jmlStokStr = txt_jmlStok.getText();
    String hargaStokStr = txt_hargaStok.getText();
    String tglStokStr = tglStok.getText(); // Mendapatkan tanggal stok dari TextField

    // Validasi jika field kosong
    if (idStokStr.isEmpty() || namaStokStr.isEmpty() || jmlStokStr.isEmpty() || hargaStokStr.isEmpty() || tglStokStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validasi input numerik untuk jumlah stok dan harga stok
    try {
        int jumlahStok = Integer.parseInt(jmlStokStr);  // Mengubah String ke Integer
        double hargaStok = Double.parseDouble(hargaStokStr);  // Mengubah String ke Double

        // Validasi format tanggal
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        java.util.Date parsedDate = sdf.parse(tglStokStr); // Parsing tanggal dari TextField
        java.sql.Date tglStoks = new java.sql.Date(parsedDate.getTime()); // Konversi ke java.sql.Date

        // Cek apakah nama stok sudah ada
        String checkQuery = "SELECT jumlah_stok, harga_stok, tanggal_stok FROM stok WHERE nama_stok = ?";

        try (Connection conn = Owner_StokInput.DatabaseConnection.getConnection();
             PreparedStatement checkPs = conn.prepareStatement(checkQuery)) {

            checkPs.setString(1, namaStokStr);
            try (ResultSet rs = checkPs.executeQuery()) {
                if (rs.next()) {
                    // Jika stok sudah ada, update jumlah stok, harga stok, dan tanggal stok
                    int existingJumlahStok = rs.getInt("jumlah_stok");
                    double existingHargaStok = rs.getDouble("harga_stok");
                    java.sql.Date existingTglStoks = rs.getDate("tanggal_stok");

                    // Update stok dan harga
                    String updateQuery = "UPDATE stok SET jumlah_stok = ?, harga_stok = ?, tanggal_stok = ? WHERE nama_stok = ?";
                    try (PreparedStatement updatePs = conn.prepareStatement(updateQuery)) {
                        updatePs.setInt(1, existingJumlahStok + jumlahStok); // Tambah jumlah stok
                        updatePs.setDouble(2, existingHargaStok + hargaStok); // Tambah harga stok
                        updatePs.setDate(3, tglStoks); // Update tanggal stok
                        updatePs.setString(4, namaStokStr);

                        int rowsUpdated = updatePs.executeUpdate();
                        if (rowsUpdated > 0) {
                            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                        }
                    }

                } else {
                    // Jika stok belum ada, insert data baru
                    String insertQuery = "INSERT INTO stok (id_stok, nama_stok, jumlah_stok, harga_stok, tanggal_stok) VALUES (?, ?, ?, ?, ?)";
                    try (PreparedStatement insertPs = conn.prepareStatement(insertQuery)) {
                        insertPs.setString(1, idStokStr);
                        insertPs.setString(2, namaStokStr);
                        insertPs.setInt(3, jumlahStok);
                        insertPs.setDouble(4, hargaStok);
                        insertPs.setDate(5, tglStoks);  // Menyimpan tanggal stok

                        int rowsInserted = insertPs.executeUpdate();
                        if (rowsInserted > 0) {
                            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }
            }

            // Mendapatkan tanggal saat ini untuk reset
            java.util.Date currentDate = new java.util.Date();  // Mendapatkan tanggal saat ini
            sdf = new java.text.SimpleDateFormat("yyyy-MM-dd"); // Format yang digunakan
            tglStok.setText(sdf.format(currentDate)); // Reset Tanggal Stok ke tanggal saat ini

            // Membersihkan field setelah sukses
            txt_ids.setText(""); // Reset ID Stok
            txt_namaStok.setSelectedIndex(0); // Reset ComboBox
            txt_jmlStok.setText(""); // Reset Jumlah Stok
            txt_hargaStok.setText(""); // Reset Harga Stok

        }

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Jumlah stok dan harga stok harus berupa angka!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (java.text.ParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Format tanggal tidak valid! Gunakan format yyyy-mm-dd.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // Pastikan pengguna memilih baris di JTable
        int selectedRow = tabel_stok.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Mendapatkan nilai dari kolom yang relevan, misalnya "id_stok"
        String idStokStr = tabel_stok.getValueAt(selectedRow, 0).toString(); // Sesuaikan indeks kolom

        // Konversi idStokStr menjadi integer jika diperlukan
        int idStok;
        try {
            idStok = Integer.parseInt(idStokStr); // Pastikan id_stok adalah integer di database
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "ID stok tidak valid!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Konfirmasi penghapusan
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
            "Apakah Anda yakin ingin menghapus data dengan ID stok: " + idStok + "?",
            "Konfirmasi Hapus",
            javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try (Connection conn = Owner_StokInput.DatabaseConnection.getConnection();
                PreparedStatement ps = conn.prepareStatement("DELETE FROM stok WHERE id_stok = ?")) {

                // Set parameter query
                ps.setInt(1, idStok);

                // Eksekusi query
                int rowsAffected = ps.executeUpdate();

                if (rowsAffected > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    loadTable(); // Refresh tabel setelah penghapusan
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

