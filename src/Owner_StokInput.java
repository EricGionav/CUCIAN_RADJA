       // Memuat data ke JTable
private void loadTable() {
    // Model untuk JTable
    DefaultTableModel model = new DefaultTableModel(
        new String[]{"ID Stok", "Nama Stok", "Jumlah Stok", "Harga Stok", "Tanggal Stok"}, 
        0
    );
    String query = "SELECT * FROM stok";

    try (Connection conn = Owner_StokInput.DatabaseConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(query);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            // Membaca data dari ResultSet
            int id_s = rs.getInt("id_stok");
            String nama_s = rs.getString("nama_stok");
            int jml_s = rs.getInt("jumlah_stok");
            int idk = rs.getInt("harga_stok");
            String tgl = rs.getString("tanggal_stok");

            // Menambahkan data ke model
            model.addRow(new Object[]{id_s, nama_s, jml_s, idk, tgl});
        }

        // Atur model ke JTable
        tabel_stok.setModel(model);
        
        setAutoIncrementId();

        // Atur ukuran kolom dan pengaturan lainnya
        Tabel(tabel_stok, new int[]{100, 150, 150, 150, 250, 150});


    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(
            this, 
            "Gagal memuat data: " + e.getMessage(), 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE
        );
    }
}


// Mengatur ukuran kolom JTable
private void Tabel(javax.swing.JTable tb, int lebar[]) {
    tb.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);  // Nonaktifkan auto-resize
    int kolom = tb.getColumnCount();
    
    // Menyesuaikan ukuran kolom sesuai lebar yang diberikan
    for (int i = 0; i < kolom; i++) {
        javax.swing.table.TableColumn tbc = tb.getColumnModel().getColumn(i);
        tbc.setPreferredWidth(lebar[i]);  // Set lebar kolom
    }

    tb.setRowHeight(25);  // Menetapkan tinggi baris
}

